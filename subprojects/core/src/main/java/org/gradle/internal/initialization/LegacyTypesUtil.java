package org.gradle.internal.initialization;

import org.gradle.api.GradleException;
import org.gradle.initialization.MixInLegacyTypesClassLoader;
import org.objectweb.asm.ClassWriter;
import org.objectweb.asm.Opcodes;
import org.objectweb.asm.Type;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.net.URL;
import java.util.HashSet;
import java.util.Set;

/**
 * Utility to enrich class loading with empty interfaces for certain types that have been removed,
 * but which are baked into the bytecode generated by the Groovy compiler.
 */
public class LegacyTypesUtil {
    public static final Set<String> CLASSES_TO_MIX_IN_GROOVY_OBJECT = readClassNames("converted-types.txt");
    public static final Set<String> SYNTHETIC_CLASSES = readClassNames("removed-types.txt");

    private static final Type OBJECT_TYPE = Type.getType(Object.class);

    private static Set<String> readClassNames(String resourceName) {
        Set<String> classNames = new HashSet<String>();
        URL resource = MixInLegacyTypesClassLoader.class.getResource(resourceName);
        try {
            BufferedReader reader = new BufferedReader(new InputStreamReader(resource.openStream()));
            try {
                String line;
                while ((line = reader.readLine()) != null) {
                    classNames.add(line.trim());
                }
            } finally {
                reader.close();
            }
        } catch (IOException e) {
            throw new GradleException("Could not load class names from '" + resource + "'.", e);
        }
        return classNames;
    }

    public static byte[] generateEmptyClass(String name) {
        ClassWriter visitor = new ClassWriter(0);
        visitor.visit(Opcodes.V1_7, Opcodes.ACC_PUBLIC | Opcodes.ACC_SYNTHETIC | Opcodes.ACC_INTERFACE | Opcodes.ACC_ABSTRACT, name.replace('.', '/'), null, OBJECT_TYPE.getInternalName(), null);
        visitor.visitEnd();
        return visitor.toByteArray();
    }

    /**
     * Injects the interfaces into an arbitrary classloader via
     * {@link ClassLoader#defineClass(String, byte[], int, int)}.
     */
    public static void injectEmptyInterfacesIntoClassLoader(ClassLoader classLoader) {
        try {
            java.lang.reflect.Method defineClassMethod =
                ClassLoader.class.getDeclaredMethod("defineClass", String.class, byte[].class, int.class, int.class);
            defineClassMethod.setAccessible(true);

            for (String name : SYNTHETIC_CLASSES) {
                byte[] bytes = generateEmptyClass(name);
                defineClassMethod.invoke(classLoader, name, bytes, 0, bytes.length);
            }

            defineClassMethod.setAccessible(false);
        } catch (Exception e) {
            throw new GradleException("Could not inject synthetic classes.", e);
        }
    }
}
